def plot_meff_with_fit(C_1d: np.ndarray, fit_result: dict, Pz: int,
                        save_dir: str, conf_id: str = "46000"):
    """Plot C2pt (log) + effective mass with best-fit band.

    Follows lamet-agent core/plotting.py conventions:
      - Times New Roman font, STIX math
      - 6.75 x 4.17 inch figure (golden ratio)
      - Color cycle: Blue/Orange/Green/Red/Violet/Fuchsia
    """
    if not HAS_MPL:
        return

    # Style setup
    plt.rcParams.update({
        "font.family": "serif",
        "mathtext.fontset": "stix",
        "font.serif": ["Times New Roman", "Times", "DejaVu Serif"],
    })

    C_pos = np.abs(C_1d) + 1e-30
    cosh_arg = (C_pos[2:] + C_pos[:-2]) / (2.0 * C_pos[1:-1])
    valid = cosh_arg >= 1.0
    meff_lu = np.full(Nt - 2, np.nan)
    meff_lu[valid] = np.arccosh(cosh_arg[valid])
    meff_gev = meff_lu * fm2GeV / alttc_fm

    fig, axes = plt.subplots(1, 2, figsize=(2 * FIG_WIDTH, FIG_WIDTH / GOLDEN_RATIO))
    t_vals = np.arange(Nt)
    t_meff = np.arange(1, Nt - 1)
    t_max = Nt // 3

    # ── Left: C2pt log-scale ──
    axes[0].errorbar(t_vals, C_pos, fmt='o', markersize=4,
                      color=BLUE, mfc='none', capsize=2)
    axes[0].set_yscale('log')
    axes[0].set_xlabel(r"$t~/~a$", fontsize=18)
    axes[0].set_ylabel(r"$C_2(t)$", fontsize=18)
    axes[0].set_title(f"2pt Correlator (Pz={Pz})", fontsize=18)
    axes[0].tick_params(direction='in', top=True, right=True, labelsize=14)
    axes[0].grid(linestyle=':', alpha=0.3)
    axes[0].set_xlim(0, t_max)

    # Fit band on C2pt panel
    best = fit_result.get("best_window")
    if best and best["status"] == "ok":
        fit_obj = best.get("fit_object")
        if fit_obj:
            tmin, tmax = best["tmin"], best["tmax"]
            fit_t = np.arange(tmin, tmax)
            fit_mean = pt2_re_fcn(fit_t, fit_obj.p, Lt=Nt)
            fit_sdev = gv.sdev(fit_mean)
            fit_mean = gv.mean(fit_mean)
            axes[0].fill_between(fit_t, fit_mean - fit_sdev, fit_mean + fit_sdev,
                                  color=ORANGE, alpha=0.3, label=f'fit [{tmin},{tmax})')
            axes[0].legend(fontsize=11, loc='upper right')

    # ── Right: Effective mass ──
    axes[1].errorbar(t_meff[valid], meff_gev[valid],
                      fmt='o', markersize=5, mfc='none', capsize=3,
                      color=BLUE, label='meff data')
    axes[1].set_xlabel(r"$t~/~a$", fontsize=18)
    axes[1].set_ylabel(r"$m_{\mathrm{eff}}~/~\mathrm{GeV}$", fontsize=18)
    axes[1].set_title(f"Effective Mass (Pz={Pz})", fontsize=18)
    axes[1].tick_params(direction='in', top=True, right=True, labelsize=14)
    axes[1].grid(linestyle=':', alpha=0.3)
    axes[1].set_xlim(0, t_max)

    # E0 horizontal band from best fit
    if best and best["status"] == "ok":
        e0_gev = best["E0_gev"]
        e0_sdev = best["E0_gev_sdev"]
        axes[1].axhline(y=e0_gev, color=RED, linestyle='--', linewidth=1,
                         label=f'E0 = {e0_gev:.4f}({e0_sdev:.4f}) GeV')
        axes[1].axhspan(e0_gev - e0_sdev, e0_gev + e0_sdev,
                          alpha=0.15, color=RED)
    axes[1].legend(fontsize=11, loc='upper right')

    plt.tight_layout()
    save_path = f"{save_dir}/meff_fit_Pz{Pz}_conf{conf_id}.pdf"
    fig.savefig(save_path, bbox_inches='tight')
    plt.close(fig)
    print(f"[PLOT] {save_path}")


def plot_e0_dispersion(results: Dict[int, Dict], save_path: str):
    """Plot E0 vs Pz dispersion relation."""
    if not HAS_MPL or not results:
        return

    plt.rcParams.update({
        "font.family": "serif",
        "mathtext.fontset": "stix",
        "font.serif": ["Times New Roman", "Times", "DejaVu Serif"],
    })

    Pz_vals = sorted(results.keys())
    E0_vals = []
    E0_errs = []
    for pz in Pz_vals:
        best = results[pz].get("best_window")
        if best and best["status"] == "ok":
            E0_vals.append(best["E0_gev"])
            E0_errs.append(best["E0_gev_sdev"])
        else:
            E0_vals.append(np.nan)
            E0_errs.append(0)

    fig, ax = plt.subplots(figsize=FIG_SIZE)
    ax.errorbar(Pz_vals, E0_vals, yerr=E0_errs, fmt='o-', markersize=6,
                 color=BLUE, capsize=4, mfc='none')
    # Overlay continuum dispersion: E(P) = sqrt(M^2 + P^2)
    Pz_gev = np.array(Pz_vals) * 2.0 * np.pi * fm2GeV / (Nx * alttc_fm)
    if len(E0_vals) > 0 and not np.isnan(E0_vals[0]):
        M0 = abs(E0_vals[0])
        P_fine = np.linspace(Pz_gev[0], Pz_gev[-1], 100)
        E_disp = np.sqrt(M0**2 + P_fine**2)
        ax.plot(Pz_vals, E_disp * (Pz_vals[-1] - Pz_vals[0]) / (P_fine[-1] - P_fine[0])
                 + E0_vals[0] - E_disp[0], '--', color=GREEN,
                 label=f'dispersion M={M0:.3f} GeV')
    ax.set_xlabel("Pz (2π/L units)", fontsize=18)
    ax.set_ylabel(r"$E_0$ (GeV)", fontsize=18)
    ax.set_title("Ground-State Energy vs Momentum", fontsize=18)
    ax.legend(fontsize=12)
    ax.tick_params(direction='in', top=True, right=True, labelsize=14)
    ax.grid(linestyle=':', alpha=0.3)

    fig.savefig(save_path, bbox_inches='tight')
    plt.close(fig)
    print(f"[PLOT] {save_path}")


