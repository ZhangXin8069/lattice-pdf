def load_2pt_data(data_dir: str, Pz: int, conf_id: str,
                   element: str = "_Cg5g4",
                   mom_smear: int = -2) -> np.ndarray:
    """Load parity-projected 2pt correlator and extract 1D time series.

    Returns C_1d[t] = mean_{t_src} C^{pp}(t_src + t, t_src).
    """
    pp_file = os.path.join(
        data_dir,
        f"twopt_slice_pp_Px0Py0Pz{Pz}"
        f"_eginphase{mom_smear}{element}_nopol_ss_conf{conf_id}.npy"
    )
    if not os.path.exists(pp_file):
        raise FileNotFoundError(f"2pt file not found: {pp_file}")

    pp_data = np.load(pp_file)          # shape (Nt, Nt)
    C_1d = np.zeros(Nt)
    for dt in range(Nt):
        vals = []
        for t_src in range(Nt):
            t_snk = (t_src + dt) % Nt
            vals.append(pp_data[t_snk, t_src])
        C_1d[dt] = np.mean(vals)        # real part

    return C_1d


