def fit_2pt_ground_state(C_1d: np.ndarray, windows: List[Tuple[int, int]],
                          nstate: int = 2, svdcut: float = 1e-3,
                          correlator_rescale: float = 1.0) -> Dict:
    """Fit 1D 2pt correlator over multiple windows and select best.

    Returns dictionary with best-fit parameters, window, and diagnostics.
    """
    if not HAS_LSQFIT:
        print("[ERROR] lsqfit + gvar not available. Install with: pip install lsqfit gvar")
        return {}

    Lt = len(C_1d)
    results = []

    for (tmin, tmax) in windows:
        fit_t = np.arange(tmin, tmax, dtype=int)
        # gvar from single-sample (placeholder for multi-conf JK later)
        fit_y = gv.gvar(C_1d[fit_t], np.ones_like(C_1d[fit_t]) * 0.01 * np.abs(C_1d[fit_t]))
        fit_y *= correlator_rescale

        prior = pt2_prior(nstate=nstate, target_E0=0.6)

        try:
            fit = lsqfit.nonlinear_fit(
                data=(fit_t, fit_y),
                prior=prior,
                fcn=lambda t, p: pt2_re_fcn(t, p, Lt=Lt, nstate=nstate) * correlator_rescale,
                svdcut=svdcut,
                maxit=10000,
            )
        except Exception as e:
            results.append({
                "tmin": tmin, "tmax": tmax,
                "status": "failed",
                "error": str(e),
                "window_label": f"[{tmin},{tmax})",
            })
            continue

        n_data = tmax - tmin
        n_params = 1 + 2 * nstate - 1  # E0 + (nstate-1)*log_dE + nstate*z
        dof = n_data - n_params if n_data > n_params else 1

        results.append({
            "tmin": tmin, "tmax": tmax,
            "status": "ok",
            "window_label": f"[{tmin},{tmax})",
            "E0": float(fit.p["E0"].mean),
            "E0_sdev": float(fit.p["E0"].sdev),
            "E0_gev": float(fit.p["E0"].mean * fm2GeV / alttc_fm),
            "E0_gev_sdev": float(fit.p["E0"].sdev * fm2GeV / alttc_fm),
            "z0": float(fit.p["z0"].mean),
            "z0_sdev": float(fit.p["z0"].sdev),
            "chi2": float(fit.chi2),
            "dof": dof,
            "chi2_per_dof": float(fit.chi2 / dof),
            "Q": float(fit.Q),
            "logGBF": float(fit.logGBF),
            "fit_object": fit,
        })

    # Select best window: filter by Q >= 0.05, minimize chi2/dof
    ok_results = [r for r in results if r["status"] == "ok" and r["Q"] >= 0.05]
    if not ok_results:
        ok_results = [r for r in results if r["status"] == "ok"]

    if not ok_results:
        return {"status": "all_failed", "results": results}

    ok_results.sort(key=lambda r: r["chi2_per_dof"])
    best = ok_results[0]

    # Also find most robust window (max dof among top chi2 windows)
    top = [r for r in ok_results if r["chi2_per_dof"] <= best["chi2_per_dof"] * 1.5]
    robust = max(top, key=lambda r: r["dof"])

    return {
        "status": "ok",
        "nstate": nstate,
        "best_window": best,
        "robust_window": robust,
        "all_results": results,
        "n_total": len(results),
        "n_ok": len(ok_results),
    }


