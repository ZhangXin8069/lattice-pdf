def generate_fit_windows(data_length: int, tmin_min: int = 2,
                          tmax_max: int = None,
                          width_steps: List[int] = None) -> List[Tuple[int, int]]:
    """Generate candidate fit windows [tmin, tmax) for ground-state scan.

    Returns all windows with tmin ∈ [2, data_length//4] and varying tmax.
    """
    if tmax_max is None:
        tmax_max = min(data_length // 2, 32)
    if width_steps is None:
        width_steps = list(range(4, tmax_max + 1, 4))
    windows = []
    for tmin in range(tmin_min, data_length // 4):
        for width in width_steps:
            tmax = tmin + width
            if tmax <= tmax_max and tmax <= data_length:
                windows.append((tmin, tmax))
    return sorted(set(windows))


