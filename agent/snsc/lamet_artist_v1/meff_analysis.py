# 2pt Spectral Model — n-state cosh decomposition
# ============================================================================

def pt2_re_fcn(t: np.ndarray, p: dict, Lt: int = Nt,
                nstate: int = 2) -> np.ndarray:
    """n-state spectral decomposition for periodic 2pt correlator.

    C(t) = sum_{n=0}^{Nst-1} z_n^2 / (2*E_n) * [e^{-E_n*t} + e^{-E_n*(Lt-t)}]

    Parameters
    ----------
    t : np.ndarray, shape (n_t,)
        Timeslice indices for evaluation
    p : dict-like (gvar.BufferDict or dict)
        Prior/fit parameters:
          E0 : ground-state energy (lattice units)
          log(dE_n) : log of gap to n-th excited state (n >= 1)
          z_n  : overlap factor for n-th state (recentered around 1)
    Lt : int
        Temporal extent of the lattice
    nstate : int
        Number of states in the spectral sum

    Returns
    -------
    C : np.ndarray or gvar array, shape (n_t,)
        2pt correlator evaluated at times t
    """
    t = np.asarray(t, dtype=float)
    E = [p["E0"]]
    for n in range(1, nstate):
        key = f"log_dE_{n}" if n > 1 else "log_dE"
        dE = np.exp(p.get(key, np.log(0.5)))
        E.append(E[-1] + dE)

    z = [p.get("z0", gv.gvar(1.0, 0.5))]
    for n in range(1, nstate):
        key = f"z_{n}"
        overlap = p.get(key, gv.gvar(0.1, 1.0) / (3**n))
        z.append(overlap)

    result = sum(
        (z_n**2) / (2.0 * E_n) *
        (np.exp(-E_n * t) + np.exp(-E_n * (Lt - t)))
        for n, (z_n, E_n) in enumerate(zip(z, E))
    )
    return result


def pt2_prior(nstate: int = 2, tmin: int = 2,
              target_E0: float = 0.5) -> dict:
    """Construct broad, physics-informed priors for 2pt fitting.

    Priors are intentionally wide (large sigma) to let data dominate:
      - E0 ~ target_E0 ± 1.0   (broad, centered on typical hadron mass)
      - log(dE) ~ log(0.5) ± 1  (O(500 MeV) level spacing)
      - z_n ~ 1.0 ± 10 / 3^n    (geometrically decreasing overlap)
    """
    prior = {}
    prior["E0"] = gv.gvar(target_E0, 1.0)
    log_dE = np.log(0.5)
    for n in range(1, nstate):
        key = f"log_dE_{n}" if n > 1 else "log_dE"
        prior[key] = gv.gvar(log_dE, 1.0)
    prior["z0"] = gv.gvar(1.0, 10.0)
    for n in range(1, nstate):
        prior[f"z_{n}"] = gv.gvar(0.1, 10.0) / (3.0**n)
    return gv.BufferDict(prior)


