def main():
    parser = argparse.ArgumentParser(
        description="lamet-agent Correlator Artist — Proton 2pt meff analysis"
    )
    parser.add_argument("--data-dir", type=str, default="./artifacts",
                        help="Directory containing 2pt .npy files")
    parser.add_argument("--conf-id", type=str, default="46000",
                        help="Configuration ID")
    parser.add_argument("--Pz-list", type=str, default="-2,-3,-4,-5,-6",
                        help="Comma-separated momentum list")
    parser.add_argument("--nstate", type=int, default=2,
                        help="Number of states in spectral fit")
    parser.add_argument("--tmin-min", type=int, default=2,
                        help="Minimum tmin for fit window scan")
    parser.add_argument("--q-min", type=float, default=0.05,
                        help="Minimum Q-value for window acceptance")
    parser.add_argument("--output-dir", type=str, default="./meff_analysis",
                        help="Output directory")
    args = parser.parse_args()

    if not HAS_LSQFIT:
        print("[ERROR] lsqfit + gvar required. Install: pip install lsqfit gvar")
        print("        Falling back to effective-mass-only mode (no fitting).")
        # Fallback: compute and plot meff without fits
        _fallback_meff_only(args)
        return

    os.makedirs(args.output_dir, exist_ok=True)
    Pz_list = [int(p) for p in args.Pz_list.split(",")]

    print(f"{'='*60}")
    print(f"  lamet-agent Correlator Artist — Meff Analysis")
    print(f"{'='*60}")
    print(f"  conf_id = {args.conf_id}")
    print(f"  Pz_list = {Pz_list}")
    print(f"  nstate = {args.nstate}")
    print(f"  data_dir = {args.data_dir}")
    print(f"  output = {args.output_dir}")
    print(f"{'='*60}\n")

    # Generate fit windows
    windows = generate_fit_windows(Nt, tmin_min=args.tmin_min, tmax_max=32)
    print(f"[WINDOWS] {len(windows)} candidate windows generated")
    print(f"  Range: tmin∈[2,{Nt//4}), width∈[4,32], step=4\n")

    all_fit_results = {}
    t_start = time.perf_counter()

    for Pz in Pz_list:
        print(f"\n{'─'*40}")
        print(f"  Pz = {Pz}")
        print(f"{'─'*40}")

        # Load data
        try:
            C_1d = load_2pt_data(args.data_dir, Pz, args.conf_id)
        except FileNotFoundError as e:
            print(f"[SKIP] {e}")
            continue

        # Determine correlator_rescale from data magnitude
        median_C0 = np.median(np.abs(C_1d[:4]))
        if median_C0 > 0:
            rescale = 10.0 ** (-int(np.floor(np.log10(median_C0))) - 3)
        else:
            rescale = 1e9
        print(f"[SCALE] correlator_rescale = {rescale:.1e}")

        # Fit ground state
        fit_result = fit_2pt_ground_state(
            C_1d, windows, nstate=args.nstate, svdcut=1e-3,
            correlator_rescale=rescale,
        )

        if fit_result["status"] == "ok":
            best = fit_result["best_window"]
            robust = fit_result["robust_window"]
            print(f"  Best  window: [{best['tmin']},{best['tmax']}) "
                  f"E0={best['E0_gev']:.4f}({best['E0_gev_sdev']:.4f}) GeV, "
                  f"χ²/dof={best['chi2_per_dof']:.2f}, Q={best['Q']:.3f}")
            print(f"  Robust:       [{robust['tmin']},{robust['tmax']}) "
                  f"χ²/dof={robust['chi2_per_dof']:.2f}, Q={robust['Q']:.3f}")
        else:
            print(f"  All {len(fit_result.get('all_results',[]))} windows failed.")
            print(f"  Falling back to meff plateau estimate.")

        all_fit_results[Pz] = fit_result

        # Save
        fit_save = {k: v for k, v in fit_result.items() if k != "all_results"}
        if "all_results" in fit_result:
            fit_save["all_results"] = [
                {kk: vv for kk, vv in r.items() if kk != "fit_object"}
                for r in fit_result["all_results"]
            ]
        with open(f"{args.output_dir}/fit_results_Pz{Pz}.json", "w") as f:
            json.dump(fit_save, f, indent=2, default=str)

        # Plot
        plot_meff_with_fit(C_1d, fit_result, Pz, args.output_dir, args.conf_id)

    # ── E0 dispersion ──
    plot_e0_dispersion(all_fit_results,
                        f"{args.output_dir}/e0_dispersion_conf{args.conf_id}.pdf")

    # ── Summary table ──
    t_end = time.perf_counter()
    print(f"\n{'═'*60}")
    print(f"  lamet-agent Analysis Complete — {t_end-t_start:.1f}s")
    print(f"{'═'*60}")
    print(f"  {'Pz':>4s}  {'E0 [GeV]':>14s}  {'χ²/dof':>8s}  {'Q':>8s}  {'Window':>12s}")
    print(f"  {'─'*56}")
    for Pz in sorted(all_fit_results.keys()):
        r = all_fit_results[Pz]
        best = r.get("best_window", {})
        if best.get("status") == "ok":
            print(f"  {Pz:4d}  {best['E0_gev']:14.6f}  "
                  f"{best['chi2_per_dof']:8.3f}  {best['Q']:8.4f}  "
                  f"[{best['tmin']},{best['tmax']})")
    print(f"{'═'*60}")


def _fallback_meff_only(args):
    """Fallback: compute and plot effective mass without lsqfit."""
