def build_gamma_matrices():
    """Construct all 18 Dirac matrices in the DeGrand-Rossi (chiral-variant) basis."""
    z = np.zeros((4, 4), dtype=complex)
    g = {}

    g[0] = z.copy()
    for i in range(4):
        g[0][i, i] = 1.0 + 0.0j  # γ₀ = I₄

    g[1] = z.copy()
    g[1][0, 3] = 0.0 + 1.0j; g[1][1, 2] = 0.0 + 1.0j
    g[1][2, 1] = 0.0 - 1.0j; g[1][3, 0] = 0.0 - 1.0j  # γ₁ = γ_x

    g[2] = z.copy()
    g[2][0, 3] = -1.0 + 0.0j; g[2][1, 2] = 1.0 + 0.0j
    g[2][2, 1] = 1.0 + 0.0j; g[2][3, 0] = -1.0 + 0.0j  # γ₂ = γ_y

    g[3] = z.copy()
    g[3][0, 2] = 0.0 + 1.0j; g[3][1, 3] = 0.0 - 1.0j
    g[3][2, 0] = 0.0 - 1.0j; g[3][3, 1] = 0.0 + 1.0j  # γ₃ = γ_z

    g[4] = z.copy()
    g[4][0, 2] = 1.0 + 0.0j; g[4][1, 3] = 1.0 + 0.0j
    g[4][2, 0] = 1.0 + 0.0j; g[4][3, 1] = 1.0 + 0.0j  # γ₄ = γ_t

    g[5] = z.copy()
    g[5][0, 0] = 1.0 + 0.0j; g[5][1, 1] = 1.0 + 0.0j
    g[5][2, 2] = -1.0 + 0.0j; g[5][3, 3] = -1.0 + 0.0j  # γ₅

    g[6] = g[2] @ g[3];   g[7] = g[3] @ g[1];   g[8] = g[1] @ g[2]
    g[9] = g[1] @ g[4];   g[10] = g[2] @ g[4];  g[11] = g[3] @ g[4]
    g[12] = g[1] @ g[5];  g[13] = g[2] @ g[5];  g[14] = g[3] @ g[5]
    g[15] = g[4] @ g[5]
    g[16] = (g[3] @ g[1]) @ (0.5 * (g[0] + g[4]))  # P₊ projected
    g[17] = (g[3] @ g[1]) @ (0.5 * (g[0] - g[4]))  # P₋ projected
    return g


