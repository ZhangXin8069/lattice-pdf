def compute_phase_factor(momentum):
    """φ_P(x) = exp(-i * 2π * P·x / Nx) for all spatial points."""
    V = Nx * Nx * Nx
    phase = np.zeros(V, dtype=complex)
    idx = 0
    for z in range(Nx):
        for y in range(Nx):
            for x in range(Nx):
                phase[idx] = np.exp(
                    -np.dot(momentum, [z, y, x]) * 2.0j * np.pi / Nx
                )
                idx += 1
    return phase


def read_eigvecs_smeared(t, phase_smear, contract_fn):
    """Read eigenvectors for timeslice t and apply momentum smearing."""
    filename = f"{EIG_DIR}/eigvecs_t{t:03d}_{CONF_ID}"
    with open(filename, "rb") as f:
        file_size = os.path.getsize(filename)
        Nev_full = int(file_size / 8 / (Nx * Nx * Nx * 3 * 2))
        data = np.fromfile(f, dtype="f8").reshape(Nev_full, Nx, Nx, Nx, 3, 2)
    eigvecs = data[..., 0] + 1j * data[..., 1]                # (Nev, Nx, Nx, Nx, 3)
    eigvecs = eigvecs[:Nev1]
    eigvecs = eigvecs.reshape(Nev_full, Nx * Nx * Nx, 3)      # (Nev, V, 3)
    eigvecs_mom = contract_fn("vxa,x->vxa", eigvecs, phase_smear)
    return eigvecs_mom


def compute_vvv_for_momentum(mom, contract_fn):
    """Compute VVV baryon block for all timeslices at given momentum."""
    # Cache check
    cache_file = f"{OUTPUT_DIR}/VVV_Nev1{Nev1}_Px{Px}Py{Py}Pz{mom[0]}_conf{CONF_ID}.npy"
    if os.path.exists(cache_file):
        print(f"[CACHE] Loading VVV: {cache_file}")
        return np.load(cache_file)

    VVV_sink = np.zeros((Nt, Nev1, Nev1, Nev1), dtype=complex)
    layer_size = Nx * Nx
    phase_P = compute_phase_factor(mom)
    phase_smear = compute_phase_factor(np.array([momsmear_phase, 0, 0]))

    for t in range(Nt):
        t1 = time.perf_counter()
        eigvecs = read_eigvecs_smeared(t, phase_smear, contract_fn)
        t2 = time.perf_counter()
        print(f"[VVV] t={t:3d} read eigvecs: {t2-t1:.3f}s")

        t3 = time.perf_counter()
        for xi in range(Nx):
            start = xi * layer_size
            end = (xi + 1) * layer_size
            ps = phase_P[start:end]
            es = eigvecs[:, start:end, :]

            VVV_sink[t] += contract_fn("x,ax,bx,cx->abc", ps, es[:, :, 0], es[:, :, 1], es[:, :, 2])
            VVV_sink[t] += contract_fn("x,ax,bx,cx->abc", ps, es[:, :, 1], es[:, :, 2], es[:, :, 0])
            VVV_sink[t] += contract_fn("x,ax,bx,cx->abc", ps, es[:, :, 2], es[:, :, 0], es[:, :, 1])
            VVV_sink[t] -= contract_fn("x,ax,bx,cx->abc", ps, es[:, :, 0], es[:, :, 2], es[:, :, 1])
            VVV_sink[t] -= contract_fn("x,ax,bx,cx->abc", ps, es[:, :, 1], es[:, :, 0], es[:, :, 2])
            VVV_sink[t] -= contract_fn("x,ax,bx,cx->abc", ps, es[:, :, 2], es[:, :, 1], es[:, :, 0])
        t4 = time.perf_counter()
        print(f"[VVV] t={t:3d} contraction: {t4-t3:.3f}s")

    np.save(cache_file, VVV_sink)
    print(f"[SAVE] VVV cache: {cache_file}")
    return VVV_sink


