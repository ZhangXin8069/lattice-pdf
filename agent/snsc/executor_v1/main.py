def contract_2pt(VVV_sink, interProject1, interProject2, contract_fn):
    """Perform full Wick contraction for all (t_src, t_snk) pairs."""
    contrac_nucl_matrix = np.zeros((Nt, Nt, 4, 4), dtype=complex)

    for t_source in range(Nt):
        t_start = time.perf_counter()
        VVV_source = VVV_sink[t_source].conj()
        peram_u = read_perambulator(t_source)

        # Dirac-projected perambulator: Γ τ Γ
        CG5peram_uCG5 = contract_fn(
            "gh,thkbe,jk->tgjbe", interProject1, peram_u, interProject2
        )

        for t_sink in range(Nt):
            deltat = (t_sink - t_source + Nt) % Nt
            if DELTAT_MIN <= deltat <= DELTAT_MAX:
                contrac_nucl_matrix[t_sink, t_source] = (
                    contract_fn(
                        "abc,gjad,gjbe,ilcf,def->il",
                        VVV_sink[t_sink], peram_u[t_sink],
                        CG5peram_uCG5[t_sink], peram_u[t_sink],
                        VVV_source,
                    )
                    - contract_fn(
                        "abc,glaf,gjbe,ijcd,def->il",
                        VVV_sink[t_sink], peram_u[t_sink],
                        CG5peram_uCG5[t_sink], peram_u[t_sink],
                        VVV_source,
                    )
                )

        t_end = time.perf_counter()
        print(f"[Wick] t_source={t_source:3d} done: {t_end-t_start:.3f}s")

    return contrac_nucl_matrix


