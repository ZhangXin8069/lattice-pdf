def apply_parity_and_boundary(contrac_nucl_matrix, gamma):
    """Apply P_plus parity projection and boundary sign correction."""
    matrix_pplus = 0.5 * (gamma[0] + gamma[4])
    matrix_pminus = 0.5 * (gamma[0] - gamma[4])

    contrac_nucl_pp = np.einsum("li,yxil->yx", matrix_pplus, contrac_nucl_matrix)
    contrac_nucl_pm = np.einsum("li,yxil->yx", matrix_pminus, contrac_nucl_matrix)

    for t_src in range(Nt):
        for t_snk in range(Nt):
            if t_snk < t_src:
                contrac_nucl_pp[t_snk, t_src] *= -1.0
            if t_snk > t_src:
                contrac_nucl_pm[t_snk, t_src] *= -1.0

    return contrac_nucl_pp, contrac_nucl_pm


def compute_effective_mass(contrac_nucl_pp):
    """Extract 1D correlator and compute cosh effective mass."""
    C_1d = np.zeros(Nt, dtype=float)
    for dt in range(Nt):
        vals = []
        for t_src in range(Nt):
            t_snk = (t_src + dt) % Nt
            vals.append(np.real(contrac_nucl_pp[t_snk, t_src]))
        C_1d[dt] = np.mean(vals)

    C_pos = np.abs(C_1d) + 1e-30
    cosh_arg = (C_pos[2:] + C_pos[:-2]) / (2.0 * C_pos[1:-1])
    valid = cosh_arg >= 1.0
    meff_lu = np.full(Nt - 2, np.nan)
    meff_lu[valid] = np.arccosh(cosh_arg[valid])
    meff_gev = meff_lu * fm2GeV / alttc_fm

    return C_1d, meff_gev


