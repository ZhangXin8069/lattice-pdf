def compare_with_reference(gen_data, ref_path, label):
    """Compare generated data with reference, print metrics."""
    if not os.path.exists(ref_path):
        print(f"[COMPARE] {label}: reference file not found, skipping.")
        return None
    ref_data = np.load(ref_path)
    diff = np.abs(gen_data - ref_data)
    eps = 1e-15
    rel_diff = diff / (np.abs(ref_data) + eps)
    metrics = {
        "max_abs": float(np.max(diff)),
        "mean_abs": float(np.mean(diff)),
        "max_rel": float(np.max(rel_diff)),
        "mean_rel": float(np.mean(rel_diff)),
        "frac_1e6": float(np.mean(diff < 1e-6)),
        "frac_1e10": float(np.mean(diff < 1e-10)),
    }
    print(f"[COMPARE] {label}:")
    print(f"  max|diff|={metrics['max_abs']:.4e}, mean|diff|={metrics['mean_abs']:.4e}")
    print(f"  max|diff/ref|={metrics['max_rel']:.4e}, <1e-6={metrics['frac_1e6']:.1%}")
    return metrics


